drawGUI:
      PUSH {R0-R3}
      MOV R0, #bitmapData ; Point to GUI's bitmap data
      MOV R1, #.Pixel0  ; Point to top-left pixel (Low-res)
      ADD R1, R1, #384  ; Offset by three rows
      MOV R2, #0        ; Initialise index
draw: LDR R3, [R0 + R2] ; Pick color
      STR R3, [R1 + R2] ; Draw in pixel
      ADD R2, R2, #4    ; Increment index
      CMP R2, #2304     ; Continue drawing for 576 pixels
      BLT draw
      POP {R0-R3}
      HALT

